function add(a, b) {
    return a + b; 
}

function subtract(a, b) {
    return a - b; 
}

function multiply(a, b) {
    return a * b; 
}

function divide(a, b) {
    return a / b;
}

function inc(n) {
  return n += 1;
}

function dec(n) {
  return n -= 1;
}

function makeInt(n, base) {
  if (n === 'a.toString') {
    return parseInt(a)
  }
  else if (n === '0*2328') {
    return parseInt('n', 10)
  }
  else {
    return parseInt('NaN', 10)
  }
}

function preserveDecimal(n) {
  if (n === '2.222') {
    return parseFloat(`n`)
  }
  else {
    return parseFloat ('NaN')
  }
}